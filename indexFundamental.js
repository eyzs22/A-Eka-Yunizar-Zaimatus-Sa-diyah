// Demonstrasi Tipe Data JavaScript

// 1. String
let teks = "Selamat datang di JavaScript!";
console.log("Tipe Data String: " + teks);

// 2. Boolean
let benar = true;
console.log("Tipe Data Boolean: " + benar);

// 3. Number
let angka = 20;
console.log("Tipe Data Number: " + angka);

// 4. Array
let buah = ["Apel", "Jeruk", "Mangga"];
console.log("Tipe Data Array: " + buah.join(", "));

// 5. Object
let mahasiswa = {
    nama: "Eka Yunizar Zaimatus Sa'diyah",
    programStudi: "Pendidikan Teknologi Informasi"
};
console.log("Tipe Data Object:\nNama: " + mahasiswa.nama + "\nProgram Studi: " + mahasiswa.programStudi);